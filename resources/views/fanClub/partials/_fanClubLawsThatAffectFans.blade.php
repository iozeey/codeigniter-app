        <h2>Bill of Rights&nbsp;</h2>
        <ul>
            <li> You have a right to complain about ticket unfairness. Please see out auto complaint system. </li>
            <li> You have a right to ticket contentment (happiness and satisfaction together). </li>
            <li> You have the right to locate a ticket at the best price and seating locations for the event you want. </li>
            <li> You have a right to use the power of the Fair2Fan method to locate these fair cost tickets.</li>
            <li> You have a right to become part of the Fair2Fan movement. </li>
            <li> You have the right to submit a ticket request to all potential Primary and Secondary sale points. Please see out auto request feature. </li>
            <li> You have a right to the lowest fees possible. In Fair2Fan's case we charge 0 fees to either the buyer or the seller to ensure our members always get the best deal. </li>
            <li> You have a right to be heard by your artist or team. Fair2Fan will always fight for bona-fide fans. </li>
        </ul>
